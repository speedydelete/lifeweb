static inline void get_rule(char* out) {
    #if STATES > 2
    int next_char = 0;
    next_char = unparse_transitions(out, next_char, true);
    out[next_char++] = '/';
    next_char = unparse_transitions(out, next_char, false);
    out[next_char++] = '/';
    sprintf(&out[next_char], "%i", STATES);
    #else
    int next_char = 0;
    out[next_char++] = 'B';
    next_char = unparse_transitions(out, next_char, false);
    out[next_char++] = '/';
    out[next_char++] = 'S';
    next_char = unparse_transitions(out, next_char, true);
    #endif
}


struct {
    bool flip_x;
    bool flip_y;
    bool rotate_left;
    bool rotate_right;
    bool rotate_180;
    bool flip_diagonal;
    bool flip_anti_diagonal;
} rule_symmetry;

static inline void get_rule_symmetry(void) {
    bool C2 = true;
    bool C4 = true;
    bool D2h = true;
    bool D2v = true;
    bool D2s = true;
    bool D2b = true;
    for (int i = 0; i < 512; i++) {
        int j = ((i << 6) & 448) | (i & 56) | (i >> 6);
        j = ((j & 73) << 2) | (j & 146) | ((j & 292) >> 2);
        if (trs[i] != trs[j]) {
            C2 = false;
            C4 = false;
            break;
        }
    }
    if (C2) {
        for (int i = 0; i < 512; i++) {
            if (trs[i] != trs[((i >> 2) & 66) | ((i >> 4) & 8) | ((i >> 6) & 1) | ((i << 2) & 132) | ((i << 6) & 256) | ((i << 4) & 32) | (i & 16)]) {
                C4 = false;
                break;
            }
        }
    }
    for (int i = 0; i < 512; i++) {
        if (trs[i] != trs[((i & 73) << 2) | (i & 146) | ((i & 292) >> 2)]) {
            D2h = false;
        }
        if (trs[i] != trs[((i << 6) & 448) | (i & 56) | (i >> 6)]) {
            D2v = false;
        }
        if (trs[i] != trs[(i & 84) | ((i << 8) & 256) | ((i >> 8) & 1) | ((i >> 4) & 10) | ((i << 4) & 160)]) {
            D2s = false;
        }
        if (trs[i] != trs[(i & 273) | ((i >> 2) & 34) | ((i >> 4) & 4) | ((i << 2) & 136) | ((i << 4) & 64)]) {
            D2b = false;
        }
    }
    rule_symmetry.flip_x = D2h;
    rule_symmetry.flip_y = D2v;
    rule_symmetry.rotate_left = C4;
    rule_symmetry.rotate_right = C4;
    rule_symmetry.rotate_180 = C2;
    rule_symmetry.flip_diagonal = D2b;
    rule_symmetry.flip_anti_diagonal = D2s;
}
